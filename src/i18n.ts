export const supportedLanguages = [
  { code: "en", labelKey: "language.en" },
  { code: "it", labelKey: "language.it" },
] as const;

export function translate(language: LanguageCode, key: string, values?: Record<string, string | number>) {
  const dictionary = dictionaries[language] ?? dictionaries.en;
  const template = dictionary[key] ?? dictionaries.en[key] ?? key;
  if (!values) return template;
  return Object.entries(values).reduce(
    (text, [name, value]) => text.split(`{${name}}`).join(String(value)),
    template,
  );
}

export function resolveInitialLanguage(): LanguageCode {
  const locale = navigator.language.toLowerCase();
  return locale.startsWith("it") ? "it" : "en";
}
